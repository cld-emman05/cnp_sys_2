            'address' => $request->input('address'),
            'contact' => $request->input('contact'),
            'email' => $request->input('email'),
            'password' => bcrypt($request->input('password')),
            'updated_at' => \Carbon\Carbon::now(),
          ]);

        return redirect('/')->with('success', 'Profile Updated!');
